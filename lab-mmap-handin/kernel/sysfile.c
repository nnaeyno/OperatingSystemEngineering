uint64
sys_munmap(void){
  struct proc* p = myproc();
  //munmap(addr, length)
  uint64 addr;
  int length;
  argaddr(0, &addr);
  argint(1, &length);
  for(int i = 0; i < NUMVMA; i++){
    if(p->vmas[i].length != 0 && p->vmas[i].addr <= addr && PGROUNDUP(p->vmas[i].addr + p->vmas[i].length) > addr){ // addr falls here
      if(p->vmas[i].flags == MAP_SHARED){
        struct file* f = p->vmas[i].fl;
        begin_op();
        ilock(f->ip);
        writei(f->ip, 1, addr, addr - p->vmas[i].addr, length);
        iunlock(f->ip);
        end_op(); //from filewrite
      }

      uvmunmap(p->pagetable, addr, length/PGSIZE, 1);
      
      if (p->vmas[i].addr == addr){
        if(p->vmas[i].end  == addr + length){ 
          changeRefCount(p->vmas[i].fl, -1);
          p->vmas[i].length = 0;
        } else {
          p->vmas[i].addr = addr + length; //needed end bc of this line
        }
      }
      return 0;
    }
  }
  return -1;
}




int wrapper(uint64 va){
  struct proc* p = myproc(); 

  struct vma* i = p->vmas;
  int found = 0;
  while(i < p->vmas + NUMVMA && !found){
    if(i->length != 0 && (i->addr + i->length) > va && i->addr <= va)
      found = 1;
    else
      i++;
  }
  
  if(!found){
    return -1;
  }
  char* ka =  kalloc();
  if(!ka){
    return -1;
  } else
    memset(ka, 0, PGSIZE);
    
   
  va = PGROUNDDOWN(va);
  uint64 pte = 0;
  if(i->prot & PROT_EXEC)  
    pte |= PTE_X;
  if(i->prot & PROT_READ)  
    pte |= PTE_R;
  if(i->prot & PROT_WRITE) 
    pte |= PTE_W;
  pte |= PTE_U;

  struct file* file = i->fl;
  begin_op();
  ilock(file->ip);
  int read = readi(file->ip, 0, (uint64)ka, va - (uint64)(i->addr), PGSIZE);
  if(read < 0){
    iunlock(file->ip);
    end_op();
    return -1;
  }
  iunlock(file->ip);
  end_op();
  if(mappages(p->pagetable, va, PGSIZE,  (uint64)ka, pte)){
    kfree((void*)ka);
    return -1;
  }
  return 0;
}
